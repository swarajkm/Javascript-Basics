//basic syntax to create objects in javascript

/* syntax is:
    -> we enclose an object within curly brackets
    -> objects are stored in the form of key-value pairs
    -> to the left of : symbol is the object properties
    -> to the right of : symbol is the object values
    -> these pairs are separated by comma
    -> and as usual, end of curly brackets we put semi colon.
*/
var ourDog = {
  name: "Casper",
  legs: 4,
  brain: 2.5,
  friends: ["everyone"],
};
