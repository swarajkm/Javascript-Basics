//HERE WE WILL SEE HOW TO UPDATE OBJECT PROPERTIES USING DOT NOTATION

var dotNotation = {
  hat: "cowboy",
  shirt: "hawaii print",
  shoes: "sneakers",
};

//we simply assign the changed value using = operator
dotNotation.hat = "gunslinger";

//and we can also output to console and entire object just like this!
console.log(dotNotation);
