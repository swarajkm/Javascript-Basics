//concatenation using + operator
var myStr1 = "I am " + "first in class"; //concat two strings
var myStr2 = "But I dont study";
var myStr3 = myStr1 + " " + myStr2 + " believe me!"; //concat strings as well as variables

console.log(myStr3);

//concatenation using += operator
var ourStr = "I come first. ";
ourStr += "I come second.";
//This just takes the string I come second and concats to string stored in ourStr

console.log(ourStr);
